nama = "Syahril"  # * contoh string
umur = 18         # * contoh integer
tinggi = 1.7599     # * contoh float/desimal
angka = 23.7882123
# saya_mahasiswa = true
# f adalah string atau formatted string literal. Ini adalah cara untuk menyisipkan nilai variabel ke dalam string dengan mudah.
print(f"Halo!, nama saya {nama} saya berumur {umur} dan tinggi saya {tinggi}")
print(f'tinggi= {tinggi:.2f}')
print(f"angka = {angka:.3f}") # digunakan untuk membatasi jumlah angka di belakang koma menjadi tiga angka desimal.
print(f"angka= {angka}") # mencetak semua angka desimalnya.