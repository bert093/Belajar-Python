inputPengguna = int(input("Masukkan angka: "))
inputPengguna2 = int(input("Masukkan angka2: "))

hasil1 = inputPengguna + inputPengguna2
hasil2 = inputPengguna - inputPengguna2
hasil3 = inputPengguna * inputPengguna2
hasil4 = inputPengguna / inputPengguna2
print(f'Hasil penjumlahan: {hasil1}')
print(f'Hasil penjumlahan: {hasil2}')
print(f'Hasil penjumlahan: {hasil3}')
print(f'Hasil penjumlahan: {hasil4}')